void setVoltage (char *buffer, float32_t voltage) {
  int16_t pwc = 0;

  if(voltage > 0) {
      pwc = (int16_t) (83.296855952222570f * voltage + 6.699955398806081f);
  } else {
      pwc = (int16_t) (84.226798317384890f * voltage - 21.820956466774298f);
  }

  sprintf(buffer, "N1 O d%d\n", (int)pwc);
  SendBuffer(&huart2, buffer);
}

float32_t StringToFloat(const char *str) {
  float32_t floatNumber = 0.0f, fraction = 0.1f, sign = 1.0f;
  int checkFraction = 0;

  if (*str != 'V') {
    return 0.0f;  // Dữ liệu lỗi thì gửi giá trị 0.0 sẽ an toàn
  }

  str++;

  if (*str == '-') { // Kiểm tra dấu âm
    sign *= -1.0f;
    str++;
  }

  while (*str) {
    if (*str == '.') {  // Nếu gặp dấu '.'
      checkFraction = 1;
    }
    else if (*str >= '0' && *str <= '9') {  // Chỉ xử lý số
      float32_t eachNumber = (float32_t)(*str - '0');
      if (checkFraction) { // Xử lý phần thập phân
        floatNumber += eachNumber * fraction;
        fraction *= 0.1;
      } else {
        floatNumber = floatNumber * 10.0f + eachNumber;  // Xử lý phần nguyên
      }
    }
    str++;
  }

  return sign*floatNumber;
}

